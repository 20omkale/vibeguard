# ─── Smart Interview ────────────────────────────────────────────────────────────

def _interview_user(client, raw_idea: str) -> dict:
    """
    Interview the user like a senior PM would.
    Extract enough information to write a professional PRD.
    """
    console.print("\n[bold yellow]🎤 Phase 1: Requirements Interview[/bold yellow]")
    console.print("[dim]I'll ask you a few questions so I can build this exactly right.[/dim]\n")

    # Generate smart questions based on the idea
    question_prompt = f"""You are a Senior Product Manager at a top tech company.
A client/developer gave you this rough idea: "{raw_idea}"

Generate 6 targeted questions that will help you understand exactly what to build.
Cover: target users, core features, scale, tech preferences, monetization, timeline.
Keep questions SHORT and clear. Non-technical person should understand them.

Return ONLY JSON:
{{
  "app_category": "web app / mobile / CLI / API / etc",
  "questions": [
    {{"id": 1, "question": "Who is the primary user of this?", "example": "e.g. Students, small business owners, developers"}},
    {{"id": 2, "question": "...", "example": "..."}}
  ]
}}"""

    with Progress(SpinnerColumn(), TextColumn("Analyzing your idea..."), transient=True) as p:
        p.add_task("", total=None)
        response = client.chat(
            messages=[{"role": "user", "content": question_prompt}],
            temperature=0.3, max_tokens=1000,
        )

    qdata = _parse_json(response)
    questions = qdata.get("questions", [])

    answers = {"raw_idea": raw_idea, "app_category": qdata.get("app_category", "web app")}

    for q in questions:
        qtext = q.get("question", "")
        example = q.get("example", "")
        if not qtext:
            continue
        hint = f"[dim]{example}[/dim]" if example else ""
        if hint:
            console.print(f"  {hint}")
        answer = Prompt.ask(f"  [cyan]{qtext}[/cyan]")
        answers[qtext] = answer
        console.print()

    return answers


