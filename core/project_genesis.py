def _generate_prd(client, answers: dict, output_dir: Path) -> str:
    """Generate a professional PRD (Product Requirements Document)."""
    context = _format_answers(answers)

    prompt = f"""You are a Senior Product Manager at Google/Amazon level.
Write a complete, professional PRD for this project.

PROJECT CONTEXT:
{context}

Write a detailed PRD with these sections:
1. Executive Summary
2. Problem Statement  
3. Target Users & Personas
4. Core Features (with priority: P0/P1/P2)
5. User Stories ("As a [user], I want to [action] so that [benefit]")
6. Non-Functional Requirements (performance, security, scale)
7. Success Metrics & KPIs
8. Out of Scope (for v1)

Make it production-level. Think like this will serve 1 million users.
Write in clean Markdown. Be specific, not generic."""

    with Progress(SpinnerColumn(), TextColumn("Writing PRD..."), transient=True) as p:
        p.add_task("", total=None)
        prd = client.chat(messages=[{"role": "user", "content": prompt}], temperature=0.3, max_tokens=4000)

    path = output_dir / "PRD.md"
    path.write_text(f"# Product Requirements Document\n*Generated by VibeGuard Genesis — {datetime.now().strftime('%Y-%m-%d')}*\n\n{prd}", encoding="utf-8")
    return str(path)


